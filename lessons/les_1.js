/*
                                  ОПЕРАТОРЫ

Программа в яваскрипт - это коллекция операторов. 
-Операторы объединяют в выражения, чтобы они выполняли общуюю задачу.
-Оператор состоит из одного или нескольких выражений, ключевых слов или операторов(символов).
-Оператор записывается в одну строку(можно в несколько строк). 
-Два и более операторов можно записать в одну строку, разделяя (;).
-Новая строка начинает новый оператор. 
-Операторы завершают явным образом точкой с запятой (;).

Пример:
*/
var aName = "Anton"; //assign text "Anton" to var aName
var today = new Date(); //assign Date(obj) to var today


/*
                                ОПЕРАТОРЫ

-Группа операторов в фигурных скобках(braces)  {...} - называется блоком.
-Операторы объединенные в блок - можно считать одним оператором. Значит, блоки можно использовать в местах, где требуется 1 один оператор.
-Операторы в блоке завершаются точкой с запятой (;), сам блок - нет.
-Блоки обычно используют в функциях и УСЛОВНЫХ ВЫРАЖЕНИЯХ.

Пример: (блок ELSE содержит 2 оператора. Блок(функции) обрабатывается как один оператор
        Операторы вне блока функции не являются частью определения функции)
*/

function inchestometers(inches)
    {
    if (inches < 0)
    return -1;
    else
        {
        var meters = inches / 39.37;
        return meters;
        }
    }

var inches = 120;
var meters = inchestometers(inches)
document.write("the value in meters = " + meters + "<br>")


