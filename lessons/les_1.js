/*
ПРИСВАИВАНИЯ И РАВЕНСТВО

-Знак равенства (=) - оператор присваивания, используется для присваивания значений переменным.
Левый операнд оператора (=) - является левосторонним значением. Примеры левосторонних значений: 
    * переменные
    * элементы массива
    * свойства объекта
Правый операнд оператора (=) - правостороннее значение. Правостороннее значение может быть любого типа, 
                                включая значения выражения.

Пример: 
*/

var anInteger = 3;


/* 
                                ВЫРАЖЕНИЯ

-Значение выражения может быть любого допустимого типа JavaScript - число, строка, объект и т.д.
Простейшими выражениями являются литералы (ЛИТЕРАЛАМИ - называют представления некотрого типа данных)

3.9                             * numeric literal
"Hello!"                        * string literal
false                           * boolean literal
null                            * literal null value
{x:1, y:2}                      * Object literal
[1,2,3]                         * Array literal
function(x){return x*x;}        * function literal


-Более сложные выражения могут содержать переменные, вызовы функции и другие выражения.
Для объединения сложных выражений можно использовать операторы (+ сложение, - вычитание, * умножение, / деление, и т.д)

Примеры:
*/
var radius = 100;
var anExpression = 3 * (4 / 5) + 6;
var aSecondExpression = Math.PI * radius * radius;
var aThirdExpression = aSecondExpression + "%" + anExpression;
var aFourthExpression = "(" + aSecondExpression + ") % (" + anExpression + ")";


